  $(document).ready(function() {
    Number.prototype.format = function(n, x, s, c) {
    var re = '\\d(?=(\\d{' + (x || 3) + '})+' + (n > 0 ? '\\D' : '$') + ')',
        num = this.toFixed(Math.max(0, ~~n));
    
    return (c ? num.replace('.', c) : num).replace(new RegExp(re, 'g'), '$&' + (s || '.'));
    };
    var option_table = {
    paging: true,
    ordering: false,
    searching: true,
    columnDefs: [
      {
        width: "6%",
        className: "dt-center",
        targets: [0]
      },
      {
        className: "dt-center",
        targets: [1,2,6]
      },
      {
        width: "20%",
        className: "dt-center",
        targets: [3,4,5]
      },
    ]
  };

  var table_data = $("#table-data").DataTable(option_table);

  $("#gender").change(function(){
    var gender = $(this).val();
    renderData(gender)
  })

  renderData("");

  function renderData(gender){
    $.ajax({
      method: "POST",
      url: "<?= base_url(); ?>Setoran/getListDataSetoran/",
      data: {gender}
    }).done(function(retval){
      table_data.clear().draw(false);
      var res = JSON.parse(retval);
      //console.log(res);
      var listData = res['listData'];
      var rows = [];
      var grandTotal=0;
      for (var i = 0; i < listData.length; i++) {
        var no = i+1;
        var id_jamaah = listData[i]['id_jamaah'];
        var nama = listData[i]['nama_jamaah'];
        var gender = listData[i]['gender'];
        var provinsi = listData[i]['nama_provinsi'];
        var frekuensi=parseInt(listData[i]['frekuensi']);
        var rerata=parseInt(listData[i]['rerata']);
        var total=parseInt(listData[i]['total']);
        var komunitas = listData[i]['singkatan_komunitas'];
        grandTotal += total;

        var btn_id = `<button type="button" class="form-control btn btn-primary btn-sm btn-progress" id_jamaah="${id_jamaah}">${id_jamaah}</button>`;

        rows.push([
          no,
          btn_id,
          nama,
         /* `${nama} (${gender})`,*/
         /* gender,*/
          /*provinsi,*/
          frekuensi.format(),
          total.format(),
          rerata.format(),
          komunitas
        ]);
      }


      $("grand_total_sholawat").text(grandTotal.format());
      table_data.rows.add(rows).draw(false);
    })
  }

  $("#table-data").on('click','.btn-progress',function(){
    var id_jamaah = $(this).attr('id_jamaah');

    $("#progressModal").find(".modal-body").load("<?= base_url(); ?>Setoran/viewProgress/",{
      id_jamaah
    })
    $("#progressModal").modal('show');
  })

  $("#dashboard_1").load("<?= base_url(); ?>Setoran/viewDashboard_1/");


    
})
